#include "level.h"
using namespace std;

//constructor
Level::Level() {
	loadLevel();
}

//draw level
void Level::draw(RenderWindow& window) {
	for (int i = 0; i < walls.size(); i++) {
		window.draw(walls[i]); // access each wall by its index
	}
}

